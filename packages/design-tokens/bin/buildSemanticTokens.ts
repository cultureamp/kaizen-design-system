import fs from 'fs'
import path from 'path'
import { semanticColorTokens } from '../src/js/semanticColorTokens'

const CSS_OUTPUT_DIR = `${process.cwd()}/css`
const SASS_OUTPUT_DIR = `${process.cwd()}/sass`

const AUTOGENERATED_HEADER = '/** THIS IS AN AUTOGENERATED FILE **/'

/**
 * Flatten all 4 semantic colour groups into a single [key, value] list,
 * dropping any entry whose value is `null` (unmapped token).
 */
const flatEntries = Object.values(semanticColorTokens)
  .flatMap((group) => Object.entries(group))
  .filter((entry): entry is [string, string] => entry[1] !== null)

const run = (): void => {
  fs.mkdirSync(CSS_OUTPUT_DIR, { recursive: true })
  fs.mkdirSync(SASS_OUTPUT_DIR, { recursive: true })

  // css/semantic.css — CSS custom properties on :root
  const cssLines = flatEntries.map(([key, value]) => `  --${key}: ${value};`)
  fs.writeFileSync(
    path.resolve(CSS_OUTPUT_DIR, 'semantic.css'),
    `${AUTOGENERATED_HEADER}\n\n:root {\n${cssLines.join('\n')}\n}\n`,
  )

  // Inline the same semantic vars into the primitives entrypoint (variables.css)
  // so every consumer of variables.css gets them automatically — no extra import.
  // Runs after build:json (which regenerates variables.css), and is idempotent
  // via the marker so re-running build:semantic alone won't duplicate the block.
  const VARIABLES_PATH = path.resolve(CSS_OUTPUT_DIR, 'variables.css')
  const SEMANTIC_MARKER = '/* --- Semantic colour tokens (autogenerated) --- */'
  if (fs.existsSync(VARIABLES_PATH)) {
    const existing = fs.readFileSync(VARIABLES_PATH, 'utf8')
    const markerIdx = existing.indexOf(SEMANTIC_MARKER)
    const primitives = markerIdx === -1 ? existing : existing.slice(0, markerIdx)
    const semanticBlock = `${SEMANTIC_MARKER}\n:root {\n${cssLines.join('\n')}\n}\n`
    fs.writeFileSync(VARIABLES_PATH, `${primitives.trimEnd()}\n\n${semanticBlock}`)
  }

  // sass/semantic.scss — SCSS variables that fall back through the CSS var
  const scssLines = flatEntries.map(([key, value]) => `$${key}: var(--${key}, ${value});`)
  fs.writeFileSync(
    path.resolve(SASS_OUTPUT_DIR, 'semantic.scss'),
    `${AUTOGENERATED_HEADER}\n\n${scssLines.join('\n')}\n`,
  )

  const totalTokens = Object.values(semanticColorTokens).flatMap((group) =>
    Object.values(group),
  ).length
  const nullCount = totalTokens - flatEntries.length

  // eslint-disable-next-line no-console -- intentional build-script progress output
  console.log(
    `Semantic tokens written: ${flatEntries.length} emitted, ${nullCount} null-skipped (${totalTokens} total).`,
  )
}

run()
