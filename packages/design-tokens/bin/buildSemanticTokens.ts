import fs from 'fs'
import path from 'path'
import { semanticColorCssVariableLines, semanticColorTokens } from '../src/js/semanticColorTokens'

const CSS_OUTPUT_DIR = `${process.cwd()}/css`
const SASS_OUTPUT_DIR = `${process.cwd()}/sass`

const AUTOGENERATED_HEADER = '/** THIS IS AN AUTOGENERATED FILE **/'

/**
 * Flatten all 4 semantic colour groups into a single [key, value] list,
 * dropping any entry whose value is `null` (unmapped token).
 */
const flatEntries = Object.values(semanticColorTokens)
  .flatMap((group) => Object.entries(group))
  .filter((entry): entry is [string, string] => entry[1] !== null)

const run = (): void => {
  fs.mkdirSync(CSS_OUTPUT_DIR, { recursive: true })
  fs.mkdirSync(SASS_OUTPUT_DIR, { recursive: true })

  // css/semantic-color.css — standalone CSS custom properties on :root.
  // The same block is also emitted directly into variables.css by build:json
  // (via the shared semanticColorCssVariableLines helper), so consumers of
  // variables.css get the semantic vars without importing this file.
  fs.writeFileSync(
    path.resolve(CSS_OUTPUT_DIR, 'semantic-color.css'),
    `${AUTOGENERATED_HEADER}\n\n:root {\n${semanticColorCssVariableLines().join('\n')}\n}\n`,
  )

  // sass/semantic-color.scss — SCSS variables that fall back through the CSS var
  const scssLines = flatEntries.map(([key, value]) => `$${key}: var(--${key}, ${value});`)
  fs.writeFileSync(
    path.resolve(SASS_OUTPUT_DIR, 'semantic-color.scss'),
    `${AUTOGENERATED_HEADER}\n\n${scssLines.join('\n')}\n`,
  )

  // css/untitled-ui-vars.css — aliases Untitled UI's `--color-*` var names to
  // Kaizen's semantic vars, so UUI components/code referencing `var(--color-bg-primary)`
  // resolve to Kaizen values. Derived from the same token source — never drifts.
  const uuiAliasLines = flatEntries.map(([key]) => `  --color-${key}: var(--${key});`)
  fs.writeFileSync(
    path.resolve(CSS_OUTPUT_DIR, 'untitled-ui-vars.css'),
    `${AUTOGENERATED_HEADER}\n\n:root {\n${uuiAliasLines.join('\n')}\n}\n`,
  )

  // css/tailwind-v4.css — TW4-native (>=4.1) `@utility` blocks so pure CSS-first
  // consumers get Kaizen's CLEAN semantic utilities (`bg-primary`, `text-primary`,
  // `fg-primary`, `border-primary`) without the TW3 preset / `@config` bridge.
  // Clean names cannot come from a stock `@theme --color-*` block (that would emit
  // the doubled `bg-bg-primary` form), so we author explicit @utility blocks.
  // The `--<name>` vars themselves come from semantic-color.css / variables.css —
  // load one of those alongside this file.
  const cssProperty = (key: string): string => {
    const category = key.slice(0, key.indexOf('-'))
    switch (category) {
      case 'bg':
        return 'background-color'
      case 'border':
        return 'border-color'
      // `text` and `fg` (foreground/icon) both set the text colour.
      default:
        return 'color'
    }
  }
  const utilityBlocks = flatEntries.map(
    ([key]) => `@utility ${key} {\n  ${cssProperty(key)}: var(--${key});\n}`,
  )
  fs.writeFileSync(
    path.resolve(CSS_OUTPUT_DIR, 'tailwind-v4.css'),
    `${AUTOGENERATED_HEADER}\n\n${utilityBlocks.join('\n\n')}\n`,
  )

  const totalTokens = Object.values(semanticColorTokens).flatMap((group) =>
    Object.values(group),
  ).length
  const nullCount = totalTokens - flatEntries.length

  // eslint-disable-next-line no-console -- intentional build-script progress output
  console.log(
    `Semantic tokens written: ${flatEntries.length} emitted, ${nullCount} null-skipped (${totalTokens} total).`,
  )
}

run()
