import fs from 'fs'
import path from 'path'
import { toCustomPropertiesString } from 'object-to-css-variables'
import { format } from 'prettier'
import { tokens } from '../src/js'
import { semanticColorCssVariableLines } from '../src/js/semanticColorTokens'
import { makeCssVariableDefinitionsMap } from '../src/lib/makeCssVariableDefinitionsMap'
import { makeCSSVariableTheme } from '../src/lib/makeCssVariableTheme'

const SEMANTIC_MARKER = '/* --- Semantic colour tokens (autogenerated) --- */'

const JSON_OUTPUT_DIR = `${process.cwd()}/tokens`
const CSS_OUTPUT_DIR = `${process.cwd()}/css`

const formatJson = (jsonString: string): Promise<string> => format(jsonString, { parser: 'json' })

/**
 * Semantic colour tokens are merged into `tokens.color` for the JS export and
 * Tailwind preset, but they are emitted as CSS variables separately (see
 * `semanticColorCssVariableLines` / `build:semantic`) under their canonical
 * names â€” `--bg-*`, `--text-*`, `--fg-*`, `--border-*`.
 *
 * They must NOT leak into the primitive `color.json`, because `build:sass` /
 * `build:less` would then generate `$color-bg-*` entries referencing an
 * undefined `--color-bg-*` variable (the real one is `--bg-*`).
 *
 * Tokens are merged under both their kebab (`bg-primary`) and camelCase
 * (`bgPrimary`) forms, so match both â€” the camel form is kebab-cased back to
 * `$color-bg-*` by `json-to-flat-sass`.
 */
const SEMANTIC_COLOR_KEY_PATTERN = /^(bg|text|fg|border)(-|[A-Z])/
const stripSemanticColorKeys = <T extends Record<string, unknown>>(color: T): T =>
  Object.fromEntries(
    Object.entries(color).filter(([key]) => !SEMANTIC_COLOR_KEY_PATTERN.test(key)),
  ) as T

const run = async (): Promise<void> => {
  fs.mkdirSync(JSON_OUTPUT_DIR, { recursive: true })
  fs.mkdirSync(CSS_OUTPUT_DIR, { recursive: true })

  // Write CSS vars tokens to CSS format
  const cssVars = toCustomPropertiesString(makeCssVariableDefinitionsMap(tokens))

  // Emit the semantic colour tokens (`--bg-*`, `--text-*`, `--fg-*`, `--border-*`)
  // in the SAME write, so variables.css is always complete from this one script.
  // (Previously build:semantic appended them afterwards, which meant running
  // build:json alone silently dropped every semantic var.)
  const semanticBlock = `${SEMANTIC_MARKER}\n:root {\n${semanticColorCssVariableLines().join('\n')}\n}\n`

  const primitivesCss = `
      /** THIS IS AN AUTOGENERATED FILE **/

      :root{${cssVars}}
    `

  fs.writeFileSync(
    path.resolve(CSS_OUTPUT_DIR, 'variables.css'),
    `${primitivesCss.trimEnd()}\n\n${semanticBlock}`,
  )

  /**
   * augmentedThemeWithCSSVariableValuesVersion
   * It will look something like:
   * ```
   * {
   *    color: {
   *       wisteria: {
   *         100: "var(--kz-var-color-wisteria-100, #012345)",
   *         100-id: "--kz-var-color-wisteria-100",
   *         100-rgb: "var(--kz-var-color-wisteria-100-rgb, 000, 111, 222)",
   *         100-rgb-id: "--kz-var-color-wisteria-100-rgb",
   *       }
   *    },
   *
   *    "kz-var": {
   *      color: {
   *          wisteria: {
   *            100: "var(--kz-var-color-wisteria-100, #012345)",
   *            100-rgb-params: "var(--kz-var-color-wisteria-100-rgb-params, 000, 111, 222)",
   *          }
   *        }
   *     }
   *     ...
   *     ...
   * }
   * ```
   */
  const augmentedThemeWithCSSVariableValuesVersion = makeCSSVariableTheme(tokens)

  /* Write JSON tokens */
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'color.json'),
    await formatJson(
      JSON.stringify({
        dataViz: augmentedThemeWithCSSVariableValuesVersion.dataViz,
        color: stripSemanticColorKeys(augmentedThemeWithCSSVariableValuesVersion.color),
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'border.json'),
    await formatJson(
      JSON.stringify({
        border: augmentedThemeWithCSSVariableValuesVersion.border,
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'animation.json'),
    await formatJson(
      JSON.stringify({
        animation: augmentedThemeWithCSSVariableValuesVersion.animation,
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'layout.json'),
    await formatJson(
      JSON.stringify({
        layout: tokens.layout,
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'shadow.json'),
    await formatJson(
      JSON.stringify({
        shadow: augmentedThemeWithCSSVariableValuesVersion.shadow,
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'spacing.json'),
    await formatJson(
      JSON.stringify({
        spacing: augmentedThemeWithCSSVariableValuesVersion.spacing,
      }),
    ),
  )
  fs.writeFileSync(
    path.resolve(JSON_OUTPUT_DIR, 'typography.json'),
    await formatJson(
      JSON.stringify({
        typography: augmentedThemeWithCSSVariableValuesVersion.typography,
      }),
    ),
  )
}

run()
